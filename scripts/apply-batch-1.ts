async function applyPhonetics() {
    const { supabaseAdmin } = await import('../lib/supabase');
    console.log(`Applying batch of ${Object.keys(PHONETICS).length} phonetics...`);

    let updated = 0;
    for (const [id, hint] of Object.entries(PHONETICS)) {
        const { error } = await supabaseAdmin
            .from('names')
            .update({ phonetic_hint: hint })
            .eq('id', parseInt(id));

        if (error) {
            console.error(`Error updating ID ${id}:`, error.message);
        } else {
            updated++;
            if (updated % 50 === 0) process.stdout.write('.');
        }
    }
    console.log(`\nSuccessfully updated ${updated} names.`);
}

applyPhonetics();
